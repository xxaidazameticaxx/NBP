-- Creates tables and constraints (shared NBP schema tables are excluded).
-- Run while connected as NBPT3.

-- Lookup & organization

CREATE TABLE NBP_ROOM (
  ID       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME     VARCHAR2(100) NOT NULL,
  BUILDING VARCHAR2(100) NOT NULL
);

CREATE TABLE NBP_DEPARTMENT (
  ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME        VARCHAR2(255) NOT NULL,
  CODE        VARCHAR2(50)  NOT NULL,
  DESCRIPTION VARCHAR2(1000)
);

ALTER TABLE NBP_DEPARTMENT
  ADD CONSTRAINT UQ_DEPARTMENT_CODE UNIQUE (CODE);


-- Academic structure

CREATE TABLE NBP_STUDY_PROGRAM (
  ID             NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME           VARCHAR2(255) NOT NULL,
  CODE           VARCHAR2(50)  NOT NULL,
  DEPARTMENT_ID  NUMBER NOT NULL,
  DURATION_YEARS NUMBER,
  DEGREE_TYPE    VARCHAR2(50)
);

ALTER TABLE NBP_STUDY_PROGRAM
  ADD CONSTRAINT UQ_STUDY_PROGRAM_CODE UNIQUE (CODE);


-- User profiles (references shared NBP.NBP_USER)

CREATE TABLE NBP_PROFESSOR (
  ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  USER_ID          NUMBER NOT NULL,
  TITLE            VARCHAR2(100),
  DEPARTMENT_ID    NUMBER NOT NULL,
  OFFICE_LOCATION  VARCHAR2(255)
);

ALTER TABLE NBP_PROFESSOR
  ADD CONSTRAINT UQ_PROFESSOR_USER UNIQUE (USER_ID);

CREATE TABLE NBP_STUDENT (
  ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  USER_ID          NUMBER NOT NULL,
  INDEX_NUMBER     VARCHAR2(50) NOT NULL,
  STUDY_PROGRAM_ID NUMBER NOT NULL,
  ENROLLMENT_YEAR  NUMBER
);

ALTER TABLE NBP_STUDENT
  ADD CONSTRAINT UQ_STUDENT_USER UNIQUE (USER_ID);

ALTER TABLE NBP_STUDENT
  ADD CONSTRAINT UQ_STUDENT_INDEX UNIQUE (INDEX_NUMBER);


-- Courses & scheduling

CREATE TABLE NBP_COURSE (
  ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME          VARCHAR2(255) NOT NULL,
  CODE          VARCHAR2(50)  NOT NULL,
  PROFESSOR_ID  NUMBER NOT NULL,
  DEPARTMENT_ID NUMBER NOT NULL,
  ACADEMIC_YEAR VARCHAR2(20),
  SEMESTER      NUMBER,
  CREDITS       NUMBER
);

ALTER TABLE NBP_COURSE
  ADD CONSTRAINT UQ_COURSE_CODE UNIQUE (CODE);

CREATE TABLE NBP_TIMETABLE (
  ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  COURSE_ID   NUMBER NOT NULL,
  ROOM_ID     NUMBER NOT NULL,
  DAY_OF_WEEK VARCHAR2(20) NOT NULL,
  START_TIME  TIMESTAMP NOT NULL,
  END_TIME    TIMESTAMP NOT NULL,
  VALID_FROM  DATE,
  VALID_TO    DATE
);

CREATE TABLE NBP_COURSE_SESSION (
  ID                 NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  COURSE_ID           NUMBER NOT NULL,
  SESSION_START_TIME  TIMESTAMP NOT NULL,
  SESSION_END_TIME    TIMESTAMP NOT NULL,
  SESSION_CODE        VARCHAR2(50),
  ROOM_ID             NUMBER NOT NULL,
  TIMETABLE_ID        NUMBER,
  SESSION_TYPE        VARCHAR2(50)
);


-- Enrollment, attendance & communication

CREATE TABLE NBP_ENROLLMENT (
  ID              NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  STUDENT_ID      NUMBER NOT NULL,
  COURSE_ID       NUMBER NOT NULL,
  ENROLLMENT_DATE DATE
);

ALTER TABLE NBP_ENROLLMENT
  ADD CONSTRAINT UQ_ENROLLMENT_STUDENT_COURSE UNIQUE (STUDENT_ID, COURSE_ID);

CREATE TABLE NBP_ATTENDANCE (
  ID               NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  STUDENT_ID       NUMBER NOT NULL,
  COURSE_SESSION_ID NUMBER NOT NULL,
  IS_PRESENT       NUMBER(1) NOT NULL,
  MARKED_AT        TIMESTAMP,
  NOTES            VARCHAR2(500),
  CONSTRAINT CK_ATTENDANCE_IS_PRESENT CHECK (IS_PRESENT IN (0,1))
);

ALTER TABLE NBP_ATTENDANCE
  ADD CONSTRAINT UQ_ATTENDANCE_STUDENT_SESSION UNIQUE (STUDENT_ID, COURSE_SESSION_ID);

CREATE TABLE NBP_ABSENCE_EXCUSE (
  ID               NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  STUDENT_ID       NUMBER NOT NULL,
  COURSE_SESSION_ID NUMBER NOT NULL,
  REASON           VARCHAR2(2000),
  SUBMITTED_AT     TIMESTAMP,
  STATUS           VARCHAR2(20),
  REVIEWED_BY      NUMBER
);

CREATE TABLE NBP_NOTIFICATION (
  ID                NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  USER_ID            NUMBER NOT NULL,
  TITLE              VARCHAR2(255) NOT NULL,
  MESSAGE            VARCHAR2(2000),
  IS_READ            NUMBER(1) DEFAULT 0 NOT NULL,
  CREATED_AT         TIMESTAMP,
  NOTIFICATION_TYPE  VARCHAR2(50),
  COURSE_SESSION_ID  NUMBER,
  CONSTRAINT CK_NOTIFICATION_IS_READ CHECK (IS_READ IN (0,1))
);

CREATE TABLE NBP_USER_SESSION (
  SESSION_ID VARCHAR2(36) PRIMARY KEY,
  USER_ID    NUMBER NOT NULL,
  CREATED_AT TIMESTAMP,
  EXPIRES_AT TIMESTAMP
);


